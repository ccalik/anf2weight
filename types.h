#ifndef TYPES_H
#define TYPES_H


typedef char			s8;
typedef s8*				ps8;
typedef unsigned char	u8;
typedef u8*				pu8;

typedef short			s16;
typedef s16*			ps16;
typedef unsigned short	u16;
typedef u16*			pu16;

typedef int				s32;
typedef s32*			ps32;
typedef unsigned int	u32;
typedef u32*			pu32;
			
typedef long long			s64;
typedef s64*				ps64;
typedef unsigned long long	u64;
typedef u64*				pu64;


#endif // TYPES_H
